#include "vector3d.hpp"

Vector3D::Vector3D()
    : x(0.0), y(0.0), z(0.0)
{
}

// -----------------------------------------------------------------------

Vector3D::Vector3D(double a)
    : x(a), y(a), z(a)
{
}

// -----------------------------------------------------------------------

Vector3D::Vector3D(double _x, double _y, double _z)
    : x(_x), y(_y), z(_z)
{
}

// -----------------------------------------------------------------------

Vector3D::Vector3D(const Vector3D &v)
    : x(v.x), y(v.y), z(v.z)
{
}

// -----------------------------------------------------------------------

Vector3D::~Vector3D()
{
}
