#include "normal.hpp"

Normal::Normal()
    : x(0.0), y(0.0), z(0.0)
{
}

// -----------------------------------------------------------------------

Normal::Normal(double a)
    : x(a), y(a), z(a)
{
}

// -----------------------------------------------------------------------

Normal::Normal(double _x, double _y, double _z)
    : x(_x), y(_y), z(_z)
{
}

// -----------------------------------------------------------------------

Normal::Normal(const Normal& n)
    : x(n.x), y(n.y), z(n.z)
{
}

// -----------------------------------------------------------------------

Normal::Normal(const Vector3D& v)
    : x(v.x), y(v.y), z(v.z)
{
}

// -----------------------------------------------------------------------

Normal::~Normal()
{
}
